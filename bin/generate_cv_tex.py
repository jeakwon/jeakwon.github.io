def escape_latex(text: str) -> str:
    """Escape special LaTeX characters in plain text."""
    if not text:
        return ""
    text = str(text)
    # Normalize already-escaped LaTeX sequences before escaping
    text = text.replace("\\&", "&")
    text = text.replace("\\%", "%")
    text = text.replace("\\$", "$")
    text = text.replace("\\#", "#")
    text = text.replace("\\_", "_")
    # Now escape
    text = text.replace("\\", "\\textbackslash{}")
    for char in "&%$#_{}":
        text = text.replace(char, f"\\{char}")
    text = text.replace("~", "\\textasciitilde{}")
    text = text.replace("^", "\\textasciicircum{}")
    return text


def load_cv_data() -> list:
    with open(CV_YML, "r", encoding="utf-8") as f:
        return yaml.safe_load(f)


def load_publications() -> list:
    with open(PAPERS_BIB, "r", encoding="utf-8") as f:
        content = f.read()
    content = re.sub(r"^---\s*\n---\s*\n", "", content)
    parser = bibtexparser.bparser.BibTexParser(common_strings=True)
    bib_db = bibtexparser.loads(content, parser=parser)
    return bib_db.entries


def format_author_list(author_string: str) -> str:
    """Convert BibTeX authors to academic format: 'J. Kwon*, L.F. Vecchietti, ...'
    Self (Kwon) is bolded. First author * marker is preserved."""
    authors = [a.strip() for a in author_string.split(" and ")]
    formatted = []
    for author in authors:
        if author.lower() == "others":
            formatted.append("et al.")
            continue

        # Detect markers
        is_first = "*" in author
        is_corresponding = "#" in author

        # Strip markers for parsing
        clean = author.replace("*", "").replace("#", "")

        # Parse "Last, First"
        if "," in clean:
            parts = clean.split(",", 1)
            last = parts[0].strip()
            first = parts[1].strip() if len(parts) > 1 else ""
        else:
            words = clean.strip().split()
            if len(words) > 1:
                last = words[-1]
                first = " ".join(words[:-1])
            else:
                last = clean.strip()
                first = ""

        # Abbreviate first names
        initials = ". ".join(w[0] for w in first.split() if w) + "." if first else ""
        short = f"{initials} {escape_latex(last)}".strip()

        # Add markers
        if is_first:
            short += "*"
        if is_corresponding:
            short += "\\#"

        # Bold self
        if SELF_LAST_NAME in last:
            short = f"\\textbf{{{short}}}"

        formatted.append(short)

    return ", ".join(formatted)


def _self_is_first(author_string: str) -> bool:
    """True if Self has a '*' marker (first or co-first author)."""
    for a in author_string.split(" and "):
        if SELF_LAST_NAME in a:
            return "*" in a
    return False


def _self_is_corresponding(entry: dict) -> bool:
    """True if Self has a '#' marker in author field (corresponding author)."""
    for a in entry.get("author", "").split(" and "):
        if SELF_LAST_NAME in a and "#" in a:
            return True
    return False


_SUFFIX_MAP = {
    "-Workshop": "-w",
    "-Findings": "-f",
    "-Blogpost": "-b",
}


def _short_label(entry: dict) -> str:
    """Compact venue+year label, e.g. 'ICML2026', 'ICLR2025-w', 'ACL2026-f'."""
    abbr = entry.get("abbr", "") or entry.get("ENTRYTYPE", "")
    year = entry.get("year", "")
    abbr = re.sub(r"\s*\([^)]*\)", "", abbr).strip()
    suffix = ""
    for long_form, short in _SUFFIX_MAP.items():
        if long_form in abbr:
            abbr = abbr.replace(long_form, "")
            suffix = short
            break
    abbr = abbr.replace(" ", "")
    return f"{abbr}{year}{suffix}"


def _clean_title(raw_title: str) -> str:
    """Strip math/braces and escape title for LaTeX."""
    title = raw_title
    math_spans = re.findall(r"\$[^$]+\$", title)
    placeholders = {}
    for i, m in enumerate(math_spans):
        ph = f"MTHPHLDR{i}ZZ"
        placeholders[ph] = m
        title = title.replace(m, ph, 1)
    title = title.replace("{", "").replace("}", "")
    title = escape_latex(title)
    for ph, m in placeholders.items():
        title = title.replace(ph, m)
    return title


def _render_entry(entry: dict) -> list:
    """Render a single bib entry as LaTeX lines in academic format."""
    authors = format_author_list(entry.get("author", ""))
    title = _clean_title(entry.get("title", ""))
    venue = entry.get("journal", "") or entry.get("booktitle", "")
    venue = escape_latex(venue)
    year = entry.get("year", "")
    highlight = entry.get("highlight", "")
    venue_line = f"\\textit{{{venue}}}, {year}"
    if highlight:
        venue_line += f" ({escape_latex(highlight)})"
    venue_line += "."
    is_selected = str(entry.get("selected", "")).lower() == "true"
    head = "\\item[\\checkmark]" if is_selected else "\\item[]"
    return [f"{head} {authors}. {title}. {venue_line}"]


def load_scholar_stats() -> dict | None:
    if not SCHOLAR_STATS_YML.exists():
        return None
    with open(SCHOLAR_STATS_YML, "r", encoding="utf-8") as f:
        return yaml.safe_load(f)


def _format_stats_date(raw: str) -> str:
    """Turn '2026-05-16' into 'as of May 2026'."""
    try:
        return "as of " + datetime.strptime(raw, "%Y-%m-%d").strftime("%B %Y")
    except (ValueError, TypeError):
        return escape_latex(raw)


def render_publication_summary(entries: list, stats: dict | None) -> str:
    """Render aggregate citation metrics + Conference/Journal breakdown."""
    # Bucket entries: "main author" = first author OR corresponding author.
    # Sort key: main venue before workshop/findings/blogpost, then year-desc.
    def _impact_key(e: dict) -> tuple:
        abbr = e.get("abbr", "")
        has_suffix = any(k in abbr for k in _SUFFIX_MAP)
        return (has_suffix, -int(e.get("year", "0") or 0))

    conf_main, conf_co = [], []
    jour_main, jour_co = [], []
    sorted_entries = sorted(entries, key=_impact_key)
    for e in sorted_entries:
        if SELF_LAST_NAME not in e.get("author", ""):
            continue
        label = _short_label(e)
        if not label:
            continue
        is_main = _self_is_first(e.get("author", "")) or _self_is_corresponding(e)
        if e.get("ENTRYTYPE", "") == "inproceedings":
            (conf_main if is_main else conf_co).append(label)
        elif e.get("ENTRYTYPE", "") == "article":
            (jour_main if is_main else jour_co).append(label)

    lines = ["\\block{publication summary}", "", "\\begin{itemize}"]

    # --- Citations sub-block ---
    if stats and "stats" in stats:
        s = stats["stats"]
        date = _format_stats_date(stats.get("metadata", {}).get("last_updated", ""))
        cite_line = (
            f"citations \\textbf{{{s.get('citations', '?')}}} "
            f"$\\cdot$ h-index \\textbf{{{s.get('h_index', '?')}}} "
            f"$\\cdot$ i10-index \\textbf{{{s.get('i10_index', '?')}}}"
        )
        if date:
            cite_line += f" \\hfill \\textcolor{{gray}}{{{date}}}"
        lines += [
            "\\item \\textbf{Citations}",
            "\\begin{itemize}",
            f"\\item {cite_line}",
            f"\\item \\url{{{SCHOLAR_URL}}}",
            "\\end{itemize}",
        ]

    def _sub_line(label: str, items: list) -> str:
        rendered = ",\\allowbreak\\ ".join(escape_latex(it) for it in items)
        return f"\\item \\textit{{{label}}} ({len(items)}): {rendered}"

    # --- Conferences sub-block ---
    if conf_main or conf_co:
        lines.append("\\item \\textbf{In Conferences}")
        lines.append("\\begin{itemize}\\sloppy")
        if conf_main:
            lines.append(_sub_line("main author", conf_main))
        if conf_co:
            lines.append(_sub_line("co-author", conf_co))
        lines.append("\\end{itemize}")

    # --- Journals sub-block ---
    if jour_main or jour_co:
        lines.append("\\item \\textbf{In Journals}")
        lines.append("\\begin{itemize}\\sloppy")
        if jour_main:
            lines.append(_sub_line("main author", jour_main))
        if jour_co:
            lines.append(_sub_line("co-author", jour_co))
        lines.append("\\end{itemize}")

    lines.append(
        "\\item \\textcolor{gray}{\\footnotesize Suffixes: -w = workshop, -f = findings, -b = blogpost}"
    )
    lines.append("\\end{itemize}")
    return "\n".join(lines)


_SELECTED_PRIORITY = {
    "ICML": 10,
    "ICLR": 20,
    "NeurIPS": 30,
    "AAAI": 40,
    "ACL-Findings": 50,
    "IJCV": 110,
    "Exp. Mol. Med.": 120,
    "Exp. Neurobiol.": 130,
    "Mol. Brain": 140,
}


def render_selected_publications(entries: list) -> str:
    """Compact selected list split by Conferences / Journals, sorted by venue priority."""
    selected = [e for e in entries if str(e.get("selected", "")).lower() == "true"]
    if not selected:
        return ""

    def _priority_key(e: dict) -> tuple:
        abbr = e.get("abbr", "")
        return (_SELECTED_PRIORITY.get(abbr, 999), -int(e.get("year", "0") or 0))

    selected.sort(key=_priority_key)
    confs = [e for e in selected if e.get("ENTRYTYPE", "") == "inproceedings"]
    journals = [e for e in selected if e.get("ENTRYTYPE", "") == "article"]

    def _entry_line(e: dict) -> str:
        label = _short_label(e)
        title = _clean_title(e.get("title", ""))
        highlight = e.get("highlight", "")
        line = f"\\textbf{{{label}}}: {title}"
        if highlight:
            line += f" ({escape_latex(highlight)})"
        return f"\\item {line}"

    lines = ["\\block{selected publications}", "", "\\begin{itemize}"]
    if confs:
        lines.append(f"\\item \\textbf{{In Conferences}} ({len(confs)})")
        lines.append("\\begin{itemize}")
        lines.extend(_entry_line(e) for e in confs)
        lines.append("\\end{itemize}")
    if journals:
        lines.append(f"\\item \\textbf{{In Journals}} ({len(journals)})")
        lines.append("\\begin{itemize}")
        lines.extend(_entry_line(e) for e in journals)
        lines.append("\\end{itemize}")
    lines.append("\\end{itemize}")
    return "\n".join(lines)


def render_publications(entries: list) -> str:
    """Render publications as a flowing list; selected entries get a checkmark."""
    sorted_entries = sorted(
        entries, key=lambda e: int(e.get("year", "0")), reverse=True
    )
    lines = [
        "\\block{publications}",
        "",
        "{\\small \\textcolor{gray}{\\checkmark\\ selected publication \\quad * first author \\quad \\# corresponding author}}",
        "",
        "\\begin{itemize}[leftmargin=2em,label={},itemsep=0.3em]\\sloppy",
    ]
    for entry in sorted_entries:
        lines.extend(_render_entry(entry))
    lines.append("\\end{itemize}")
    return "\n".join(lines)


def render_cv_section_items(section: dict) -> str:
    r"""Render a time_table section. Education/Experience use stacked title/institution/date layout."""
    raw_title = section.get("title", "").lower()
    title = escape_latex(raw_title)
    stacked = raw_title in {"education", "professional experience"}
    lines = [f"\\block{{{title}}}", "", "\\begin{itemize}"]

    for item in section.get("contents", []):
        year = str(item.get("year", ""))
        item_title = item.get("title", "")
        institution = item.get("institution", "")

        if item_title and institution:
            if stacked:
                line = (
                    f"\\textbf{{{escape_latex(item_title)}}}\\\\\n"
                    f"{escape_latex(institution)} \\hfill {escape_latex(year)}"
                )
                descs = item.get("description", [])
                for d in descs:
                    if isinstance(d, str):
                        line += f"\\\\\n$\\diamond$~{escape_latex(d)}"
                lines.append(f"\\item {line}")
            else:
                parts = [
                    f"\\textbf{{{escape_latex(item_title)}}}",
                    escape_latex(institution),
                    escape_latex(year),
                ]
                line = " \\sep ".join(parts)
                descs = item.get("description", [])
                desc_texts = [escape_latex(d) for d in descs if isinstance(d, str)]
                if desc_texts:
                    line += " " + " ".join(desc_texts)
                lines.append(f"\\item {line}")
        elif "items" in item:
            for award in item["items"]:
                lines.append(f"\\item {escape_latex(award)} \\sep {escape_latex(year)}")

    lines.append("\\end{itemize}")
    return "\n".join(lines)


def render_list_section(section: dict) -> str:
    """Render a list section."""
    title = escape_latex(section.get("title", "").lower())
    lines = [f"\\block{{{title}}}", "", "\\begin{itemize}"]
    for item in section.get("contents", []):
        lines.append(f"\\item {escape_latex(str(item))}")
    lines.append("\\end{itemize}")
    return "\n".join(lines)


def render_role_suffix_section(section: dict) -> str:
    """Flat list; venue followed by role in parentheses."""
    title = escape_latex(section.get("title", "").lower())
    lines = [f"\\block{{{title}}}", "", "\\begin{itemize}"]
    for item in section.get("contents", []):
        role = item.get("role", "")
        venue = escape_latex(item.get("venue", ""))
        line = f"{venue} ({escape_latex(role)})" if role else venue
        lines.append(f"\\item {line}")
    lines.append("\\end{itemize}")
    return "\n".join(lines)


def render_prefixed_section(section: dict) -> str:
    """Flat list; each item starts with a bold role prefix, then venue (year inline)."""
    title = escape_latex(section.get("title", "").lower())
    lines = [f"\\block{{{title}}}", "", "\\begin{itemize}"]
    for item in section.get("contents", []):
        role = item.get("role", "")
        venue = escape_latex(item.get("venue", ""))
        theme = item.get("theme", "")
        line = f"\\textbf{{{escape_latex(role)}}} \\sep {venue}" if role else venue
        if theme:
            line += f" \\sep {escape_latex(theme)}"
        lines.append(f"\\item {line}")
    lines.append("\\end{itemize}")
    return "\n".join(lines)


def render_nested_list(section: dict) -> str:
    """Render a nested list section."""
    title = escape_latex(section.get("title", "").lower())
    lines = [f"\\block{{{title}}}", "", "\\begin{itemize}"]
    for group in section.get("contents", []):
        items_str = ", ".join(escape_latex(i) for i in group.get("items", []))
        lines.append(f"\\item {escape_latex(group.get('title', ''))} \\sep {items_str}")
    lines.append("\\end{itemize}")
    return "\n".join(lines)


def generate_cv_tex():
    cv_data = load_cv_data()
    publications = load_publications()

    # Extract name from General Information
    name = "jea kwon"
    for section in cv_data:
        if section.get("title") == "General Information":
            for item in section.get("contents", []):
                if item.get("name") == "Name":
                    name = item["value"].lower()
            break

    # Footer
    now = datetime.now()
    date_str = now.strftime("%B %Y")

    footer = (
        f"\\hypersetup{{pdftitle={{{escape_latex(name.title())}'s CV}}}}\n"
        f"\\grayfoot{{L}}{{\\url{{{WEBSITE_URL}}}}}\n"
        f"\\grayfoot{{C}}{{\\url{{{EMAIL}}}}}\n"
        f"\\grayfoot{{R}}{{{date_str}}}\n"
    )

    # Build document
    parts = [PREAMBLE, footer, "\\begin{document}", "", f"\\name{{{name}}}"]

    # Render each cv.yml section
    for section in cv_data:
        st = section.get("type", "")
        title = section.get("title", "")
        if title == "General Information":
            continue
        if st == "time_table":
            parts.append(render_cv_section_items(section))
        elif st == "list":
            parts.append(render_list_section(section))
        elif st == "nested_list":
            parts.append(render_nested_list(section))
        elif st == "prefixed":
            parts.append(render_prefixed_section(section))
        elif st == "role_suffix":
            parts.append(render_role_suffix_section(section))

    # Publication summary (citations + venue breakdown)
    parts.append(render_publication_summary(publications, load_scholar_stats()))

    # Full Publications list (selected entries marked inline with checkmark)
    parts.append(render_publications(publications))

    parts.append("\n\\end{document}\n")

    full_tex = "\n\n".join(parts)

    with open(OUTPUT_TEX, "w", encoding="utf-8") as f:
        f.write(full_tex)
    print(f"Generated {OUTPUT_TEX}")


if __name__ == "__main__":
    generate_cv_tex()
